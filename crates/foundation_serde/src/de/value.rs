struct UnitOnlyVariantAccess<'a, E> {
    value: &'a str,
    marker: std::marker::PhantomData<E>,
}

impl<'de, 'a, E> super::EnumAccess<'de> for UnitOnlyVariantAccess<'a, E>
where
    E: Error,
{
    type Error = E;
    type Variant = UnitOnly<E>;

    fn variant_seed<V>(self, seed: V) -> Result<(V::Value, Self::Variant), Self::Error>
    where
        V: super::DeserializeSeed<'de>,
    {
        let de = StrDeserializer::new(self.value);
        let value = seed.deserialize(de)?;
        Ok((
            value,
            UnitOnly {
                marker: std::marker::PhantomData,
            },
        ))
    }
}

struct UnitOnly<E> {
    marker: std::marker::PhantomData<E>,
}

impl<'de, E> super::VariantAccess<'de> for UnitOnly<E>
where
    E: Error,
{
    type Error = E;

    fn unit_variant(self) -> Result<(), Self::Error> {
        Ok(())
    }

    fn newtype_variant<T>(self) -> Result<T, Self::Error>
    where
        T: Deserialize<'de>,
    {
        Err(Error::custom("expected unit variant"))
    }

    fn tuple_variant<V>(self, _len: usize, _visitor: V) -> Result<V::Value, Self::Error>
    where
        V: Visitor<'de>,
    {
        Err(Error::custom("expected unit variant"))
    }

    fn struct_variant<V>(
        self,
        _fields: &'static [&'static str],
        _visitor: V,
    ) -> Result<V::Value, Self::Error>
    where
        V: Visitor<'de>,
    {
        Err(Error::custom("expected unit variant"))
    }
}

