use thiserror::Error;

const BLOCK_LEN: usize = 16;
const KEY_LEN: usize = 32;
const ROUND_KEYS_LEN: usize = 240; // 16 bytes * (Nr + 1) where Nr = 14
const ROUNDS: usize = 14;

#[derive(Debug, Error, Clone, PartialEq, Eq)]
pub enum SymmetricError {
    #[error("ciphertext length must be a multiple of 16 bytes")]
    InvalidLength,
    #[error("invalid padding byte")]
    InvalidPadding,
}

pub fn encrypt_aes256_cbc(key: &[u8; KEY_LEN], iv: &[u8; BLOCK_LEN], plaintext: &[u8]) -> Vec<u8> {
    let round_keys = expand_key(key);
    let mut padded = pkcs7_pad(plaintext);
    let mut prev = *iv;
    for chunk in padded.chunks_mut(BLOCK_LEN) {
        xor_inplace(chunk, &prev);
        encrypt_block(chunk.try_into().unwrap(), &round_keys);
        prev.copy_from_slice(chunk);
    }
    padded
}

pub fn decrypt_aes256_cbc(
    key: &[u8; KEY_LEN],
    iv: &[u8; BLOCK_LEN],
    ciphertext: &[u8],
) -> Result<Vec<u8>, SymmetricError> {
    if ciphertext.len() % BLOCK_LEN != 0 {
        return Err(SymmetricError::InvalidLength);
    }
    let round_keys = expand_key(key);
    let mut out = Vec::with_capacity(ciphertext.len());
    let mut prev = *iv;
    for chunk in ciphertext.chunks(BLOCK_LEN) {
        let mut block = [0u8; BLOCK_LEN];
        block.copy_from_slice(chunk);
        let cipher_block = block;
        decrypt_block(&mut block, &round_keys);
        xor_inplace(&mut block, &prev);
        out.extend_from_slice(&block);
        prev = cipher_block;
    }
    pkcs7_unpad(&mut out)?;
    Ok(out)
}

fn pkcs7_pad(input: &[u8]) -> Vec<u8> {
    let padding = BLOCK_LEN - (input.len() % BLOCK_LEN);
    let mut out = Vec::with_capacity(input.len() + padding);
    out.extend_from_slice(input);
    out.extend(std::iter::repeat(padding as u8).take(padding));
    out
}

fn pkcs7_unpad(buffer: &mut Vec<u8>) -> Result<(), SymmetricError> {
    if buffer.is_empty() {
        return Err(SymmetricError::InvalidPadding);
    }
    let pad = *buffer.last().unwrap() as usize;
    if pad == 0 || pad > BLOCK_LEN || pad > buffer.len() {
        return Err(SymmetricError::InvalidPadding);
    }
    let start = buffer.len() - pad;
    if !buffer[start..].iter().all(|&b| b as usize == pad) {
        return Err(SymmetricError::InvalidPadding);
    }
    buffer.truncate(start);
    Ok(())
}

fn xor_inplace(block: &mut [u8], other: &[u8; BLOCK_LEN]) {
    for (a, b) in block.iter_mut().zip(other.iter()) {
        *a ^= *b;
    }
}

fn expand_key(key: &[u8; KEY_LEN]) -> [u8; ROUND_KEYS_LEN] {
    let mut expanded = [0u8; ROUND_KEYS_LEN];
    expanded[..KEY_LEN].copy_from_slice(key);
    let mut bytes_generated = KEY_LEN;
    let mut rcon = 1u8;
    let mut temp = [0u8; 4];

    while bytes_generated < ROUND_KEYS_LEN {
        temp.copy_from_slice(&expanded[bytes_generated - 4..bytes_generated]);
        if bytes_generated % KEY_LEN == 0 {
            temp = sub_word(rot_word(temp));
            temp[0] ^= rcon;
            rcon = xtime(rcon);
        } else if bytes_generated % KEY_LEN == 16 {
            temp = sub_word(temp);
        }
        for i in 0..4 {
            expanded[bytes_generated] = expanded[bytes_generated - KEY_LEN] ^ temp[i];
            bytes_generated += 1;
        }
    }

    expanded
}

fn encrypt_block(block: &mut [u8; BLOCK_LEN], round_keys: &[u8; ROUND_KEYS_LEN]) {
    add_round_key(block, &round_keys[..BLOCK_LEN]);
    for round in 1..ROUNDS {
        sub_bytes(block);
        shift_rows(block);
        mix_columns(block);
        let start = round * BLOCK_LEN;
        add_round_key(block, &round_keys[start..start + BLOCK_LEN]);
    }
    sub_bytes(block);
    shift_rows(block);
    let start = ROUNDS * BLOCK_LEN;
    add_round_key(block, &round_keys[start..start + BLOCK_LEN]);
}

fn decrypt_block(block: &mut [u8; BLOCK_LEN], round_keys: &[u8; ROUND_KEYS_LEN]) {
    let start = ROUNDS * BLOCK_LEN;
    add_round_key(block, &round_keys[start..start + BLOCK_LEN]);
    inv_shift_rows(block);
    inv_sub_bytes(block);
    for round in (1..ROUNDS).rev() {
        let start = round * BLOCK_LEN;
        add_round_key(block, &round_keys[start..start + BLOCK_LEN]);
        inv_mix_columns(block);
        inv_shift_rows(block);
        inv_sub_bytes(block);
    }
    add_round_key(block, &round_keys[..BLOCK_LEN]);
}

fn add_round_key(block: &mut [u8; BLOCK_LEN], key: &[u8]) {
    for (b, k) in block.iter_mut().zip(key.iter()) {
        *b ^= *k;
    }
}

fn sub_bytes(state: &mut [u8; BLOCK_LEN]) {
    for byte in state.iter_mut() {
        *byte = S_BOX[*byte as usize];
    }
}

fn inv_sub_bytes(state: &mut [u8; BLOCK_LEN]) {
    for byte in state.iter_mut() {
        *byte = INV_S_BOX[*byte as usize];
    }
}

fn shift_rows(state: &mut [u8; BLOCK_LEN]) {
    let mut temp = [0u8; BLOCK_LEN];
    temp.copy_from_slice(state);
    for r in 0..4 {
        for c in 0..4 {
            state[r + 4 * c] = temp[r + 4 * ((c + r) % 4)];
        }
    }
}

fn inv_shift_rows(state: &mut [u8; BLOCK_LEN]) {
    let mut temp = [0u8; BLOCK_LEN];
    temp.copy_from_slice(state);
    for r in 0..4 {
        for c in 0..4 {
            state[r + 4 * c] = temp[r + 4 * ((c + 4 - r) % 4)];
        }
    }
}

fn mix_columns(state: &mut [u8; BLOCK_LEN]) {
    for c in 0..4 {
        let col = &mut state[c * 4..(c + 1) * 4];
        let a0 = col[0];
        let a1 = col[1];
        let a2 = col[2];
        let a3 = col[3];
        col[0] = gmul(0x02, a0) ^ gmul(0x03, a1) ^ a2 ^ a3;
        col[1] = a0 ^ gmul(0x02, a1) ^ gmul(0x03, a2) ^ a3;
        col[2] = a0 ^ a1 ^ gmul(0x02, a2) ^ gmul(0x03, a3);
        col[3] = gmul(0x03, a0) ^ a1 ^ a2 ^ gmul(0x02, a3);
    }
}

fn inv_mix_columns(state: &mut [u8; BLOCK_LEN]) {
    for c in 0..4 {
        let col = &mut state[c * 4..(c + 1) * 4];
        let a0 = col[0];
        let a1 = col[1];
        let a2 = col[2];
        let a3 = col[3];
        col[0] = gmul(0x0e, a0) ^ gmul(0x0b, a1) ^ gmul(0x0d, a2) ^ gmul(0x09, a3);
        col[1] = gmul(0x09, a0) ^ gmul(0x0e, a1) ^ gmul(0x0b, a2) ^ gmul(0x0d, a3);
        col[2] = gmul(0x0d, a0) ^ gmul(0x09, a1) ^ gmul(0x0e, a2) ^ gmul(0x0b, a3);
        col[3] = gmul(0x0b, a0) ^ gmul(0x0d, a1) ^ gmul(0x09, a2) ^ gmul(0x0e, a3);
    }
}

fn gmul(a: u8, b: u8) -> u8 {
    let mut p = 0u8;
    let mut hi_bit_set;
    let mut aa = a;
    let mut bb = b;
    for _ in 0..8 {
        if (bb & 1) != 0 {
            p ^= aa;
        }
        hi_bit_set = aa & 0x80;
        aa <<= 1;
        if hi_bit_set != 0 {
            aa ^= 0x1b;
        }
        bb >>= 1;
    }
    p
}

fn rot_word(word: [u8; 4]) -> [u8; 4] {
    [word[1], word[2], word[3], word[0]]
}

fn sub_word(word: [u8; 4]) -> [u8; 4] {
    [
        S_BOX[word[0] as usize],
        S_BOX[word[1] as usize],
        S_BOX[word[2] as usize],
        S_BOX[word[3] as usize],
    ]
}

fn xtime(x: u8) -> u8 {
    if x & 0x80 == 0 {
        x << 1
    } else {
        (x << 1) ^ 0x1b
    }
}

